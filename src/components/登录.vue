<template>
  <div class="login_container">
    <div class="login_box">
      <!-- 头像区域 -->
      <div class="avatar_box">
        <img src="../assets/logo.png" alt="" />
      </div>
      <!-- 登录表单区域 ref="loginFormRef" 为实现重置 :rules="loginFormRules" 为实现校验-->
      <el-form
        ref="loginFormRef"
        :model="loginForm"
        :rules="loginFormRules"
        label-width="0px"
        class="login_form"
      >
        <!-- 用户名 -->
        <el-form-item prop="username">
          <el-input
            v-model="loginForm.username"
            prefix-icon="iconfont icon-user"
          ></el-input>
        </el-form-item>
        <!-- 密码 -->
        <el-form-item prop="password">
          <el-input
            v-model="loginForm.password"
            prefix-icon="iconfont icon-3702mima"
            type="password"
          ></el-input>
        </el-form-item>
        <!-- 按钮区域 -->
        <el-form-item class="btns">
          <el-button type="primary" @click="login">登录</el-button>
          <el-button type="info" @click="resetLoginForm">重置</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      // 这是登录表单的数据绑定对象
      loginForm: {
        username: "admin",
        password: "123456",
      },
      // 这是表单的验证规则对象
      loginFormRules: {
        // 验证用户名是否合法
        username: [
          { required: true, message: "请输入登录名称", trigger: "blur" },
          {
            min: 3,
            max: 10,
            message: "长度在 3 到 10 个字符",
            trigger: "blur",
          },
        ],
        // 验证密码是否合法
        password: [
          { required: true, message: "请输入登录密码", trigger: "blur" },
          {
            min: 6,
            max: 15,
            message: "长度在 6 到 15 个字符",
            trigger: "blur",
          },
        ],
      },
    };
  },
  methods: {
    //点击按钮 重置表单   1.在表单先加一个值 ref 引用 ，这个loginFormRef 值就是组件引用的对象  接着我们可以通过 this
    //访问到 $refs 在.loginFormRef 引用对象 在调用resetFields就可以了
    resetLoginForm() {
      this.$refs.loginFormRef.resetFields();
    },
    login() {
      // validate()  接受回调函数
      // 先获取表单的引用对像  拿着这个引用对象可以调用validate函数 进行表单的预校验
      this.$refs.loginFormRef.validate(async valid=>{
          // 判断valid的值是否为true
          if(!valid) return;
          const {data : res} =await this.$http.post('login',this.loginForm)
          if(res.meta.status != 200) return this.$message.error("登录失败")
          this.$message.success("登陆成功");
          // 把token保存到sessionStorage中
          window.sessionStorage.setItem('token',res.data.token)
          // 跳转
          this.$touter.push("/home")
      })
    },
  },
};
</script>

